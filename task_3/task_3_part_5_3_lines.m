% --- Побудова ліній рівня ---

% Створюємо нове вікно
figure;

% Будуємо контурний графік для нашої функції на заданому інтервалі
ezcontour('x*sin(y) + y*sin(x)', [-2*pi, 2*pi]);

% Додаємо форматування
title('Графік ліній рівня для z = x*sin(y) + y*sin(x)');
xlabel('Вісь X');
ylabel('Вісь Y');
grid on;
axis equal; % Вирівнюємо масштаби осей