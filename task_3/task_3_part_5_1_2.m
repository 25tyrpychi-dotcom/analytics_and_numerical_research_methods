% --- Спосіб 1 і 2: ezmesh ---

% Створюємо нове графічне вікно
figure;

% Будуємо поверхню. MATLAB автоматично використовує інтервал [-2*pi, 2*pi]
ezmesh('x*sin(y) + y*sin(x)');

% Додаємо форматування
title('Спосіб 1/2: Побудова поверхні за допомогою ezmesh');
xlabel('Вісь X');
ylabel('Вісь Y');
zlabel('Вісь Z');